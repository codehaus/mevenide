    String getOutputURL() {
        if (txtOuputPath.getText().trim().length() > 0) {
            return "http://" + txtServer.getText().trim() + (txtPort2.getText().trim().length() > 0 ? (":" + txtPort2.getText().trim()) : "") + txtOuputPath.getText().trim();
        }
        return null;
    } 
